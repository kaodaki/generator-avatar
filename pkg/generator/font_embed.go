// getChar возвращает растровые данные для символа.
// Строчные буквы автоматически маппятся на заглавные.
func getChar(r rune) [7][5]byte {
	// Проверяем прямое совпадение
	if data, ok := fontBitmap[r]; ok {
		return data
	}

	// Пробуем верхний регистр для строчных букв
	upper := toUpper(r)
	if data, ok := fontBitmap[upper]; ok {
		return data
	}

	// Возвращаем fallback
	return fallbackChar
}

// toUpper конвертирует руну в верхний регистр (только ASCII).
func toUpper(r rune) rune {
	if r >= 'a' && r <= 'z' {
		return r - 'a' + 'A'
	}
	return r
}

